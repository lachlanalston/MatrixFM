from PIL import Image, ImageDraw, ImageFont
def makeImage(user):
	out = Image.new("RGB", (1000, 1080), (255, 255, 255))
	fnt = ImageFont.truetype("FreeMono.ttf", 40)
	d = ImageDraw.Draw(out)
	d.rectangle([0,0,1000,60], width = 40, fill="#101010")
	d.rectangle([0,60,1000,120], width = 40, fill="#202020")
	d.rectangle([0,120,1000,180], width = 40, fill="#303030")
	d.rectangle([0,180,1000,240], width = 40, fill="#404040")
	d.rectangle([0,240,1000,300], width = 40, fill="#505050")
	d.rectangle([0,300,1000,360], width = 40, fill="#606060")
	d.rectangle([0,360,1000,420], width = 40, fill="#707070")
	d.rectangle([0,420,1000,480], width = 40, fill="#808080")
	d.rectangle([0,480,1000,540], width = 40, fill="#909090")
	d.rectangle([0,540,1000,600], width = 40, fill="#101010")
	d.rectangle([0,600,1000,660], width = 40, fill="#202020")
	d.rectangle([0,660,1000,720], width = 40, fill="#303030")
	d.rectangle([0,720,1000,780], width = 40, fill="#404040")
	d.rectangle([0,780,1000,840], width = 40, fill="#505050")
	d.rectangle([0,840,1000,900], width = 40, fill="#606060")
	d.rectangle([0,900,1000,960], width = 40, fill="#707070")
	d.rectangle([0,1020,1000,1080], width = 40, fill="#909090")
	d.multiline_text((10,10), user + " 20", font=fnt, fill=(0, 0, 0))
	out.show()
	
